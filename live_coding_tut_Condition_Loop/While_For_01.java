package live_coding_tut_Condition_Loop;

public class While_For_01 {
	 public static void main(String[] args) {
	/* 
	 * for(초기화; 종료조건; 반복실행){ 
	 * 반복적으로 실행될 구문 
	 } 
	 */
	 
	 // 자주 쓰는 요소를 문법화.
		/*
		 *  int i = 0; 
		 * 	while(i<10){ 
		 * 	System.out.println("Coding Everybody"+i); 
		 * 	i+=1; // i++; 
		 * }
		 */
	 	  for (int i = 0; i < 10; i++) {
	 	      System.out.println("Coding Everybody " + i);
	 	      
	 	  }
	 
	 }
	 
	
	
	
	
	
	
	
	
	
}

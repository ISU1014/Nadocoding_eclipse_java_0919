package live_coding_tutorials_DataType;

public class DataType01 {

	public static void main(String[] args) {
		//데이터의 크기
		byte a;
		a = 127; //-128~128
		
		long aa = 2147483647; // 다른 수더라도 타입이 같기때문에 메모리는 같음
		long b = 1;
		
		//상수
		int constant01 = 1;
		//1=2; 1은 상수
		
		//형변환
		int f = (int)2.2;
		double d = 2.2;
		float ff = 2.2f;
		long l =12345678910l;
		
		
	}

}

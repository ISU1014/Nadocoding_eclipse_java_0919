package live_coding_tut_Arrays;

public class _02_Array {
	 public static void main(String[] args) {
	        String[] classGroup = new String[4]; //새로운 배열 4개의 원소.
	        classGroup[0] = "최진혁";
	        System.out.println(classGroup.length); //.length 배열 원소의 개수
	        classGroup[1] = "최유빈";
	        System.out.println(classGroup.length);
	        classGroup[2] = "한이람";
	        System.out.println(classGroup.length);
	        classGroup[3] = "이고잉";
	        System.out.println(classGroup.length);
	        //length 개의 원소
	        //index 0,1,2,3
	        //element 원소 이름.
	    }

	
}
